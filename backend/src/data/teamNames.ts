/**
 * 获取球队中文名称
 * @param englishName 球队英文名
 * @returns 中文名称，如果没有映射则返回原名
 */
export function getTeamChineseName(englishName: string): string {
    // 先尝试精确匹配
    if (TEAM_NAMES[englishName]) {
        return TEAM_NAMES[englishName];
    }
    
    // 尝试去除多余空格后匹配
    const trimmedName = englishName.trim();
    if (TEAM_NAMES[trimmedName]) {
        return TEAM_NAMES[trimmedName];
    }
    
    // 尝试忽略大小写匹配
    const lowerName = englishName.toLowerCase();
    for (const [key, value] of Object.entries(TEAM_NAMES)) {
        if (key.toLowerCase() === lowerName) {
            return value;
        }
    }
    
    // 没有找到映射，返回原名
    return englishName;
}
